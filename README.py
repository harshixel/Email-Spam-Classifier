def clean_text(text):
    text = text.lower()
    text = re.sub(r'http\S+', ' ', text)
    text = re.sub(r'\S+@\S+', ' ', text)
    text = re.sub(r'[^a-z0-9\s]', ' ', text)
    text = re.sub(r'\s+', ' ', text).strip()
    return text


def tokenize(text):
    stopwords = {
        "a", "an", "the", "is", "it", "in", "on", "at", "to", "for",
        "of", "and", "or", "but", "not", "with", "this", "that", "be",
        "as", "by", "from", "are", "was", "will", "have", "has", "had",
        "do", "does", "did", "your", "you", "we", "i", "my", "our",
        "if", "its", "so", "up", "can", "just", "me", "he", "she",
        "they", "them", "their", "been", "about", "get", "got", "also"
    }
    tokens = clean_text(text).split()
    single = [t for t in tokens if t not in stopwords and len(t) > 2]
    pairs = [single[i] + "_" + single[i + 1] for i in range(len(single) - 1)]
    return single + pairs


class NaiveBayesClassifier:

    def __init__(self, alpha=1.0):
        self.alpha = alpha
        self.class_probs = {}
        self.word_probs = {}
        self.vocab = set()
        self.classes = []

    def fit(self, texts, labels):
        label_counts = defaultdict(int)
        for label in labels:
            label_counts[label] += 1

        total_docs = len(labels)
        self.classes = list(label_counts.keys())

        self.class_probs = {
            cls: np.log(label_counts[cls] / total_docs) for cls in self.classes
        }

        word_counts = {cls: defaultdict(int) for cls in self.classes}
        total_words = {cls: 0 for cls in self.classes}

        for text, label in zip(texts, labels):
            for word in tokenize(text):
                word_counts[label][word] += 1
                total_words[label] += 1
                self.vocab.add(word)

        vocab_size = len(self.vocab)

        self.word_probs = {}
        for cls in self.classes:
            self.word_probs[cls] = {}
            for word in self.vocab:
                count = word_counts[cls].get(word, 0)
                self.word_probs[cls][word] = np.log(
                    (count + self.alpha) / (total_words[cls] + self.alpha * vocab_size)
                )

    def predict_proba(self, text):
        tokens = tokenize(text)
        scores = {}
        for cls in self.classes:
            score = self.class_probs[cls]
            for word in tokens:
                if word in self.vocab:
                    score += self.word_probs[cls][word]
            scores[cls] = score

        max_score = max(scores.values())
        exp_scores = {cls: np.exp(scores[cls] - max_score) for cls in scores}
        total = sum(exp_scores.values())
        return {cls: exp_scores[cls] / total for cls in exp_scores}

    def predict(self, text):
        return max(self.predict_proba(text), key=self.predict_proba(text).get)

    def to_dict(self):
        return {
            "alpha": self.alpha,
            "classes": self.classes,
            "class_probs": self.class_probs,
            "word_probs": self.word_probs,
            "vocab": list(self.vocab),
        }

    def from_dict(self, data):
        self.alpha = data["alpha"]
        self.classes = data["classes"]
        self.class_probs = data["class_probs"]
        self.word_probs = data["word_probs"]
        self.vocab = set(data["vocab"])


class SpamTypeClassifier:

    def __init__(self):
        self.nb = NaiveBayesClassifier(alpha=0.5)

    def fit(self, texts, types):
        self.nb.fit(texts, types)

    def predict(self, text):
        return self.nb.predict(text)


def train(data):
    df = pd.DataFrame(data, columns=["text", "label", "spam_type"])

    ham_spam_clf = NaiveBayesClassifier(alpha=1.0)
    ham_spam_clf.fit(df["text"].tolist(), df["label"].tolist())

    spam_df = df[df["label"] == "spam"]
    type_clf = SpamTypeClassifier()
    type_clf.fit(spam_df["text"].tolist(), spam_df["spam_type"].tolist())

    correct = sum(1 for text, true_label, _ in data if ham_spam_clf.predict(text) == true_label)
    acc = correct / len(data) * 100
    print(f"Model trained on {len(data)} emails. Training accuracy: {acc:.1f}%")

    return ham_spam_clf, type_clf



def classify_email(text, ham_spam_clf, type_clf):
    proba = ham_spam_clf.predict_proba(text)
    spam_prob = proba.get("spam", 0.0)

    if spam_prob >= 0.5:
        spam_type = type_clf.predict(text)
        return "spam", spam_type, spam_prob
    else:
        return "ham", "none", spam_prob


